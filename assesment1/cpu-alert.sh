#!/bin/bash
cpuuse=$(cat /proc/loadavg | awk '{print $1}')
echo "+------------------------------------------------------------------+"
echo "+------------------------------------------------------------------+"
echo "+------------------------------------------------------------------+"
echo "CPU Current Usage is: $cpuuse%"
echo "+------------------------------------------------------------------+"
echo "+------------------------------------------------------------------+"
echo "+------------------------------------------------------------------+"
diskuse=$(df -H | grep -vE '^Filesystem|tmpfs|cdrom' | awk '{ print $5 " " $1 }')
echo "+------------------------------------------------------------------+"
echo "+------------------------------------------------------------------+"
echo "+------------------------------------------------------------------+"
echo "Disk Current Usage is: $diskuse%"
echo "+------------------------------------------------------------------+"
echo "+------------------------------------------------------------------+"
echo "+------------------------------------------------------------------+"
RX_Bytes=$(cat /sys/class/net/eth0/statistics/rx_bytes)
TX_bytes=$(cat /sys/class/net/eth0/statistics/tx_bytes)
echo "+------------------------------------------------------------------+"
echo "+------------------------------------------------------------------+"
echo "+------------------------------------------------------------------+"
echo "RX Bytes Count Is: $RX_Bytes"
echo "TX Bytes Count Is: $RX_Bytes"
echo "+------------------------------------------------------------------+"
echo "+------------------------------------------------------------------+"
echo "+------------------------------------------------------------------+"
